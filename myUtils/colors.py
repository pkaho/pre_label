    import numpy as np
    import matplotlib.pyplot as plt

    def find_factors(num):
        factors = []
        for i in range(1, num + 1):
            if num % i == 0:
                factors.append(i)
        sorted_nums = sorted(factors)  # 对数组进行排序
        n = len(sorted_nums)
        median = sorted_nums[n // 2]  # 奇数个数的情况
        return median

    def Hex_to_RGB(hex):
        r = int(hex[0:2], 16)
        g = int(hex[2:4], 16)
        b = int(hex[4:6], 16)
        return (r, g, b)

    def draw_matrix(colors, row, col):
        print(f"绘制{row}*{col}的色块矩阵")
        matrix = np.zeros((row, col, 3), dtype=np.uint8)
        for i in range(row):
            for j in range(col):
                value = colors[(i * col + j) % len(colors)]
                if isinstance(value, str):
                    value = Hex_to_RGB(value[1:])
                matrix[i, j] = value
        plt.imshow(matrix)
        plt.axis("off")
        plt.show()

